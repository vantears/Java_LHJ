package day5.Practice;
import java.util.Scanner;

public class DivisorEx {

	public static void main(String[] args) {
		int num;
		int i = 1;
		Scanner sc = new Scanner(System.in);
		System.out.println("Enter an integer number : ");
		num = sc.nextInt();
		while (i <= num) {
			if (num % i == 0) {
				System.out.println(i);
			}
			i++;
		}	
		sc.close();
	}
}
