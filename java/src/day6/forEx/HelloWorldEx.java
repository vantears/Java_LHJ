package day6.forEx;

public class HelloWorldEx {

	public static void main(String[] args) {
		int i;
		for(i = 1; i <= 5; i++) {
			System.out.println("Hello World!" + i);
		}
		System.out.println("=====================");
		i = 1;
		while(i <= 5) {
			System.out.println("Hello World!" + i);
			i++;
		}

	}

}
